describe('filterCitations', () => {
  it('returns only citations above threshold sorted by score', () => {
    const candidates = [
      makeCandidate({ doc_abbreviation: 'OTHER', heading: null, snippet: 'Irrelevant cooking content' }),
      makeCandidate({ doc_abbreviation: 'PHAK', heading: 'Preflight inspection', snippet: 'Airworthiness certificates and logbook entries' }),
      makeCandidate({ doc_abbreviation: 'CFR', heading: '14 CFR Part 91', snippet: 'Aircraft airworthiness requirements and certificates' }),
    ];

    const input = makeInput({ area: 'I' });
    const { kept, filtered } = filterCitations(candidates, input);

    // PHAK and CFR should be kept; OTHER should be filtered
    expect(kept.length).toBeGreaterThanOrEqual(1);
    expect(kept.length).toBeLessThanOrEqual(3);

    // kept should be sorted by score desc
    for (let i = 0; i < kept.length - 1; i++) {
      expect(kept[i].relevance.score).toBeGreaterThanOrEqual(kept[i + 1].relevance.score);
    }

    // All kept items should be above threshold
    for (const item of kept) {
      expect(item.relevance.keep).toBe(true);
    }

    // All filtered items should be below threshold
    for (const item of filtered) {
      expect(item.relevance.keep).toBe(false);
    }
  });

  it('respects maxResults parameter', () => {
    const candidates = Array.from({ length: 10 }, (_, i) =>
      makeCandidate({
        doc_abbreviation: 'PHAK',
        heading: `Chapter ${i}: Preflight certificates airworthiness`,
        snippet: `Content about preflight certificate airworthiness inspection number ${i}.`,
      })
    );

    const { kept } = filterCitations(candidates, makeInput({ area: 'I' }), 3);
    expect(kept.length).toBeLessThanOrEqual(3);
  });

  it('returns empty kept array when all are irrelevant', () => {
    const candidates = [
      makeCandidate({ doc_abbreviation: 'OTHER', heading: null, snippet: 'Cooking recipe' }),
      makeCandidate({ doc_abbreviation: 'OTHER', heading: null, snippet: 'Basketball rules' }),
    ];

    const { kept } = filterCitations(candidates, makeInput({ area: 'IX' }));
    expect(kept.length).toBe(0);
  });

  it('prefers transcript citations in tie-breaking', () => {
    const candidates = [
      makeCandidate({
        doc_abbreviation: 'PHAK',
        heading: 'Preflight Inspection',
        snippet: 'Check airworthiness certificates and documents.',
        source: 'evidence',
      }),
      makeCandidate({
        doc_abbreviation: 'PHAK',
        heading: 'Preflight Inspection',
        snippet: 'Check airworthiness certificates and documents.',
        source: 'transcript',
      }),
    ];

    const { kept } = filterCitations(candidates, makeInput({ area: 'I' }));
    expect(kept.length).toBe(2);
    // Transcript should be first (higher score due to bonus)
    expect(kept[0].citation.source).toBe('transcript');
  });
});
